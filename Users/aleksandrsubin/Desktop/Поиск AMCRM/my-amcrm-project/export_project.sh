#!/bin/bash
# Скрипт для экспорта проекта в папку на Рабочем столе

# Устанавливаем директорию назначения
DEST_DIR="$HOME/Desktop/MyProjectArchive"

# Создаем папку назначения, если она не существует
mkdir -p "$DEST_DIR"

# Копируем все файлы и папки проекта, сохраняя структуру
# Это будет работать, потому что скрипт запускается из корневой папки проекта
cp -R . "$DEST_DIR/"

echo "Проект успешно экспортирован в папку: $DEST_DIR"
echo "Вы можете найти ее на своем Рабочем столе под названием MyProjectArchive."
